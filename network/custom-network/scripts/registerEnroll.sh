  fabric-ca-client enroll -u https://auditadmin:auditadminpw@localhost:10054 --caname AuditCA -M "${PWD}/organizations/peerOrganizations/audit.treasurychain.com/users/Admin@audit.treasurychain.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/audit/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/audit.treasurychain.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/audit.treasurychain.com/users/Admin@audit.treasurychain.com/msp/config.yaml"
}

function createOrderer() {
  infoln "Enrolling the CA admin"
  mkdir -p organizations/ordererOrganizations/orderer.treasurychain.com

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:11054 --caname OrdererCA --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-11054-OrdererCA.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-11054-OrdererCA.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-11054-OrdererCA.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-11054-OrdererCA.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy orderer org's CA cert to orderer org's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem" "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/msp/tlscacerts/tlsca.orderer.treasurychain.com-cert.pem"

  # Copy orderer org's CA cert to orderer org's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem" "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/tlsca/tlsca.orderer.treasurychain.com-cert.pem"

  infoln "Registering orderer"
  set -x
  fabric-ca-client register --caname OrdererCA --id.name orderer --id.secret ordererpw --id.type orderer --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Generating the orderer MSP"
  set -x
  fabric-ca-client enroll -u https://orderer:ordererpw@localhost:11054 --caname OrdererCA -M "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/orderers/orderer.orderer.treasurychain.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/msp/config.yaml" "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/orderers/orderer.orderer.treasurychain.com/msp/config.yaml"

  infoln "Generating the orderer TLS certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://orderer:ordererpw@localhost:11054 --caname OrdererCA -M "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/orderers/orderer.orderer.treasurychain.com/tls" --enrollment.profile tls --csr.hosts orderer.orderer.treasurychain.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the orderer's tls directory that are referenced by orderer startup config
  cp "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/orderers/orderer.orderer.treasurychain.com/tls/tlscacerts/"* "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/orderers/orderer.orderer.treasurychain.com/tls/ca.crt"
  cp "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/orderers/orderer.orderer.treasurychain.com/tls/signcerts/"* "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/orderers/orderer.orderer.treasurychain.com/tls/server.crt"
  cp "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/orderers/orderer.orderer.treasurychain.com/tls/keystore/"* "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/orderers/orderer.orderer.treasurychain.com/tls/server.key"

  # Copy orderer org's CA cert to orderer's /msp/tlscacerts directory (for use in the orderer MSP definition)
  mkdir -p "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/orderers/orderer.orderer.treasurychain.com/msp/tlscacerts"
  cp "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/orderers/orderer.orderer.treasurychain.com/tls/tlscacerts/"* "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/orderers/orderer.orderer.treasurychain.com/msp/tlscacerts/tlsca.orderer.treasurychain.com-cert.pem"

  # Register and generate artifacts for the orderer admin
  infoln "Registering the orderer admin"
  set -x
  fabric-ca-client register --caname OrdererCA --id.name ordererAdmin --id.secret ordererAdminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Generating the admin msp"
  set -x
  fabric-ca-client enroll -u https://ordererAdmin:ordererAdminpw@localhost:11054 --caname OrdererCA -M "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/users/Admin@orderer.treasurychain.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/msp/config.yaml" "${PWD}/organizations/ordererOrganizations/orderer.treasurychain.com/users/Admin@orderer.treasurychain.com/msp/config.yaml"
}