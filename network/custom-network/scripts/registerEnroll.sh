#!/usr/bin/env bash

function createFinanceOrg() {
  infoln "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/finance.treasurychain.com/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/finance.treasurychain.com/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname FinanceCA --tls.certfiles "${PWD}/organizations/fabric-ca/finance/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-FinanceCA.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-FinanceCA.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-FinanceCA.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-FinanceCA.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy Finance's CA cert to Finance's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/finance/ca-cert.pem" "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/msp/tlscacerts/ca.crt"

  # Copy Finance's CA cert to Finance's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/finance/ca-cert.pem" "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/tlsca/tlsca.finance.treasurychain.com-cert.pem"

  # Copy Finance's CA cert to Finance's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/ca"
  cp "${PWD}/organizations/fabric-ca/finance/ca-cert.pem" "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/ca/ca.finance.treasurychain.com-cert.pem"

  infoln "Registering peer0"
  set -x
  fabric-ca-client register --caname FinanceCA --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/finance/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering user"
  set -x
  fabric-ca-client register --caname FinanceCA --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/finance/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering the org admin"
  set -x
  fabric-ca-client register --caname FinanceCA --id.name financeadmin --id.secret financeadminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/finance/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Generating the peer0 msp"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname FinanceCA -M "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/peers/peer0.finance.treasurychain.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/finance/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/peers/peer0.finance.treasurychain.com/msp/config.yaml"

  infoln "Generating the peer0-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname FinanceCA -M "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/peers/peer0.finance.treasurychain.com/tls" --enrollment.profile tls --csr.hosts peer0.finance.treasurychain.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/finance/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/peers/peer0.finance.treasurychain.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/peers/peer0.finance.treasurychain.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/peers/peer0.finance.treasurychain.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/peers/peer0.finance.treasurychain.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/peers/peer0.finance.treasurychain.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/peers/peer0.finance.treasurychain.com/tls/server.key"

  infoln "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:7054 --caname FinanceCA -M "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/users/User1@finance.treasurychain.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/finance/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/users/User1@finance.treasurychain.com/msp/config.yaml"

  infoln "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://financeadmin:financeadminpw@localhost:7054 --caname FinanceCA -M "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/users/Admin@finance.treasurychain.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/finance/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/finance.treasurychain.com/users/Admin@finance.treasurychain.com/msp/config.yaml"
}

